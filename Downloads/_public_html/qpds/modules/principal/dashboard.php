     JOIN subjects s ON qp.subject_id=s.id
     JOIN departments d ON s.department_id=d.id
     JOIN users u ON qp.generated_by=u.id
     ORDER BY qp.created_at DESC LIMIT 6"
);

// Dept-wise paper counts
$deptStats = $db->fetchAll(
    "SELECT d.name, d.code, COUNT(qp.id) as paper_count
     FROM departments d
     LEFT JOIN subjects s ON s.department_id=d.id
     LEFT JOIN question_papers qp ON qp.subject_id=s.id
     GROUP BY d.id ORDER BY paper_count DESC"
);

