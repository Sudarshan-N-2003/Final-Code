    "SELECT qp.*, s.subject_name, s.subject_code, d.name as dept_name, u.full_name as by_name
     FROM question_papers qp
     JOIN subjects s ON qp.subject_id=s.id
     JOIN departments d ON s.department_id=d.id
     JOIN users u ON qp.generated_by=u.id
     WHERE qp.status='pending_approval'
     ORDER BY qp.created_at DESC LIMIT 10"
);

$recentPapers = $db->fetchAll(
    "SELECT qp.*, s.subject_name, s.subject_code, d.code as dept_code, u.full_name as by_name
